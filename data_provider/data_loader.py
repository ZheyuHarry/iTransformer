class Dataset_ETT_hour(Dataset):
    def __init__(self , root_path , flag="train",size=None,
                 features="S", data_path="ETTh1.csv",
                 target="OT", scale=True, timeenc=0, freq="h"):
        """
            @param root_path:
        """
        # size [seq_len, label_len, pred_len]

        # Default or assigned
        # seq_len: Length of the input sequence
        # label_len: Length of the groundtruth sequence
        # pred_len: Length of the prediction sequence
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        # initialization
        assert flag in ['train' , 'test' , 'val']
        type_map = {'train': 0 , 'val' : 1 , "test" : 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        # Read the data from file
        df_raw = pd.read_csv(os.path.join(self.root_path , self.data_path))

        # Set the borders for train , test , val datasets
        # 这里的数字代表12(4)个月，30天，24小时，为什么集试机和验证集需要减掉一个seq_len，这是因为我们用这一段seq_len来作为预测的输入
        borders1 = [0 , 12 * 30 * 24 - self.seq_len , 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        borders2 = [12 * 30 * 24 , 12 * 30 * 24 + 4 * 30 * 24 , 12 * 30 * 24 + 8 * 30 * 24]

        # 根据数据集类型选择正确的数据边界
        border1 = borders1[self.set_type]
        border2 = borders2[self.set_type]

        if self.features == "M" or self.features == "MS":
            cols_data = df_raw.columns[1: ] # 获取所有的特征列
            df_data = df_raw[cols_data]
        else:
            df_data = df_raw[[self.target]] # 只需要目标序列进行UTS prediction

        # Scale
        if self.scale:
            train_data = df_data[borders1[0] : borders2[0]]
            self.scaler.fit(train_data.values) # 选取values属性，将DataFrame的数据转化为数组提取出来
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        # Get DateTime
        # 双中括号 [['date']] 返回的是一个 DataFrame，而单中括号 ['date'] 返回的是一个 Series 对象（即一个一维数组）, 从中选择从第 border1 行到 border2 行之间的数据
        df_stamp = df_raw[['date']][border1 : border2] 
        df_stamp['date'] = pd.to_datetime(df_stamp.date) 

        # Get Time Features
        # By Hand
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], 1).values
        # By external function
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0) # 这里需要转置是因为time_features本身是把各个特征用Vstack堆起来的，我们要把每个特征从横向变为竖向

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self , index):
        # 获取对应的时间窗口
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        # 这个是输入序列和gt序列
        seq_x = self.data_x[s_begin : s_end]
        seq_y = self.data_y[r_begin : r_end]
        # 这个是输入序列和gt序列队以哦那个的时间标记特征
        seq_x_mark = self.data_stamp[s_begin : s_end]
        seq_y_mark = self.data_stamp[r_begin : r_end]
        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1 # self.seq_len + self.pred_len是一个样本，用滑动窗口方式计算得出总共的样本数

    def inverse_transform(self , data):
        return self.scaler.inverse_transform(data) # 把统计特征还回去，对应了那个RevIN


